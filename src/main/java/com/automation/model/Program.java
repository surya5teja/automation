public class Program {

	private String affiliate;
	private String brandProgramStatus;
	private String dtpcStatus;
	private String assignedClaudia;
	private String reimbursementSpecialist;
	private String dtpcIndication;

	public String getAffiliate() {
		return affiliate;
	}

	public void setAffiliate(String affiliate) {
		this.affiliate = affiliate;
	}

	public String getBrandProgramStatus() {
		return brandProgramStatus;
	}

	public void setBrandProgramStatus(String brandProgramStatus) {
		this.brandProgramStatus = brandProgramStatus;
	}

	public String getDtpcStatus() {
		return dtpcStatus;
	}

	public void setDtpcStatus(String dtpcStatus) {
		this.dtpcStatus = dtpcStatus;
	}

	public String getAssignedClaudia() {
		return assignedClaudia;
	}

	public void setAssignedClaudia(String assignedClaudia) {
		this.assignedClaudia = assignedClaudia;
	}

	public String getReimbursementSpecialist() {
		return reimbursementSpecialist;
	}

	public void setReimbursementSpecialist(String reimbursementSpecialist) {
		this.reimbursementSpecialist = reimbursementSpecialist;
	}

	public String getDtpcIndication() {
		return dtpcIndication;
	}

	public void setDtpcIndication(String dtpcIndication) {
		this.dtpcIndication = dtpcIndication;
	}

}
